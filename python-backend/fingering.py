def nota_a_espanol(note: str) -> str:
    """Convierte nota en inglés a español. Ej: C4 -> Do4, Bb3 -> Sib3"""
    for i, c in enumerate(note):
        if c.isdigit():
            letra = note[:i]
            octava = note[i:]
            nombre = NOTAS_ESP.get(letra, letra)
            return f"{nombre}{octava}"
    return note


def get_fingering(instrument: str, note: str, octave: str) -> str:
    key = f"{note}{octave}"
    instrument = instrument.lower().replace(" ", "_")
    if instrument in DIGITACIONES:
        return DIGITACIONES[instrument].get(key, "?")
    return "?"


def analyze_xml(xml_content: str, instrument: str) -> dict:
    results = []
    try:
        root = ET.fromstring(xml_content)

        # Obtener nombre de la canción
        title = ""
        for credit in root.iter('credit-words'):
            if credit.text:
                title = credit.text.strip()
                break
        if not title:
            work = root.find('.//work-title')
            if work is not None and work.text:
                title = work.text.strip()
        if not title:
            movement = root.find('.//movement-title')
            if movement is not None and movement.text:
                title = movement.text.strip()

        # Solo tomar la primera parte (pentagrama superior)
        parts = root.findall('part')
        first_part = parts[0] if parts else root
        for note in first_part.iter('note'):
            if note.find('chord') is not None:
                continue
            pitch = note.find('pitch')
            if pitch is not None:
                step = pitch.findtext('step', '')
                octave = pitch.findtext('octave', '')
                alter = pitch.findtext('alter', '')
                if alter == '1':
                    step = step + '#'
                elif alter == '-1':
                    step = step + 'b'

                note_key = f"{step}{octave}"
                fingering = get_fingering(instrument, step, octave)
                nombre_esp = nota_a_espanol(note_key)

                results.append({
                    "note": note_key,
                    "note_esp": nombre_esp,
                    "fingering": fingering
                })

    except Exception as e:
        return {"error": str(e)}

    return {
        "notes": results,
        "title": title
    }